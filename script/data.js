const shuffleAlbums = (arr) => {
  let newAlbumsData = [...arr]
  for (let i = newAlbumsData.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [newAlbumsData[i], newAlbumsData[j]] = [newAlbumsData[j], newAlbumsData[i]];
  }
  return newAlbumsData
}

